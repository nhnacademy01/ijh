public class unary2 {
    public static void main(String[] args) {
        int i = 1;
        System.out.println(i++);
        System.out.println(i);
        System.out.println(++i);
        System.out.println("------");
        System.out.println(--i);
        System.out.println(i);
        System.out.println(i--);
        System.out.println(i);
    }
}
