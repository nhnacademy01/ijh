public class SpecialChars {
    public static void main(String[] args) {
        System.out.println('\'');                   // '
        System.out.println("nhn\tacademy.\bcom");   // tab, backspace
        System.out.println('\n');                   // new-line(System. lineSeparator())
        System.out.println("\"Hello\"");            // "
        System.out.println("c:\\");                 // \
    }
}